# arithmetic operators

# add, subtract, multiply, divide, modulus, exponent, floor division
# +, -, *, /, %, **, //

x = 10
y = 3

print(x+y) # 10+3 = 13
print(x-y) # 10-3 = 7
print(x*y) # 10*3 = 30
print(x/y) # 10/3 = 3.333..
print(x%y) # 10%3 = 1
print(x**y) # 10x10x10 = 1000
print(x//y) # 10//3 = 3

# comparison operators

# >, <, ==, !=, >=, <=

x = 10
y = 3

print(x>y) #true
print(x<y) #false
print(x==y) #false
print(x!=y) #true
print(x>=y) #true
print(x<=y) #false

# logical operators
# and, or, not

a,b = True,False

print(a and b) #false
print(a or b) #true
print(not a) #false


# assignment operators
# +=, -=, *=, /=

x = 5

x += 2 # x = 5+2 = 7
x -= 3 # x = 5-3 = 2
x *= 5 # x = 5*5 = 25
x /= 2 # x = 5/2 = 2.5


# membership operators
# in, not in

nums = [1, 2, 3]
print(2 in nums) #true
print(8 not in nums) #true


# identity operators
# is, not is

x = [1, 2, 3]
y = x

print(x is y) #true
print(x is not y) #false




